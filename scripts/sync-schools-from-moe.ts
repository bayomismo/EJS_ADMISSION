// Helper: slugify a string to a unique city/governorate search key
function norm(s: string): string {
  return s.replace(/\s+/g, " ").trim();
}

async function main() {
  console.log(`🌱 Syncing ${SCHOOLS.length} schools from MoE 2026/2027 list...\n`);

  // Ensure all governorates exist (they should from seed)
  const govCache = new Map<string, string>();
  const allGovs = await db.governorate.findMany();
  for (const g of allGovs) {
    govCache.set(norm(g.nameAr), g.id);
  }
  console.log(`Governorates in DB: ${allGovs.length}`);
  const missingGovs = new Set<string>();
  for (const [gov] of SCHOOLS) {
    if (!govCache.has(norm(gov))) missingGovs.add(gov);
  }
  if (missingGovs.size > 0) {
    console.error("❌ Missing governorates:", [...missingGovs]);
    process.exit(1);
  }

  // Cache existing cities by (governorateId, nameAr)
  const cityCache = new Map<string, string>();
  const allCities = await db.city.findMany();
  for (const c of allCities) {
    cityCache.set(`${c.governorateId}|${norm(c.nameAr)}`, c.id);
  }

  async function ensureCity(govId: string, name: string): Promise<string> {
    const key = `${govId}|${norm(name)}`;
    const cached = cityCache.get(key);
    if (cached) return cached;
    const c = await db.city.upsert({
      where: { governorateId_nameAr: { governorateId: govId, nameAr: name } },
      create: { governorateId: govId, nameAr: name, nameEn: name, isActive: true, sortOrder: 999 },
      update: {},
    });
    cityCache.set(key, c.id);
    return c.id;
  }

  // Collect all unique keys we want active (by nameAr) so we can
  // mark non-listed schools inactive at the end.
  const keepByKey = new Set<string>(); // key = clean name (with stripped suffix)
  let created = 0, updated = 0, skipped = 0;

  for (const [govName, rawName, address] of SCHOOLS) {
    const govId = govCache.get(norm(govName))!;
    const type = extractType(rawName);
    const cleanName = stripTypeSuffix(rawName);
    const cityName = CITY_OVERRIDES[rawName] || cleanName;

    // Ensure city exists
    const cityId = await ensureCity(govId, cityName);

    const key = cleanName;
    keepByKey.add(key);

    // Try to find an existing school matching the clean name + governorate
    // (case-insensitive Arabic match via exact equality after normalize)
    const matchByName = await db.school.findFirst({
      where: { nameAr: cleanName, governorateId: govId },
    });

    const data = {
      nameAr: cleanName,
      type,
      addressAr: address || null,
      governorateId: govId,
      cityId,
      isActive: true,
      isArchived: false,
    };

    if (matchByName) {
      // Update in place — preserve existing code (already URL-safe)
      await db.school.update({ where: { id: matchByName.id }, data });
      updated++;
      console.log(`  ✓ ${govName} | ${cleanName} (updated, code=${matchByName.code})`);
    } else {
      // New school — generate code
      let code = makeCode(govName, cleanName, "EJS-SCH");
      let suffix = 1;
      while (suffix < 50) {
        const conflict = await db.school.findUnique({ where: { code } });
        if (!conflict) break;
        suffix++;
        code = makeCode(govName, cleanName, "EJS-SCH").replace(/-(\d+)$/, `-${1000 + suffix}`);
      }
      await db.school.create({ data: { ...data, code } });
      created++;
      console.log(`  + ${govName} | ${cleanName} (new, code=${code})`);
    }
  }

  // Soft-delete any active school not in the official list
  const allActive = await db.school.findMany({ where: { isActive: true, isArchived: false } });
  const toDeactivate = allActive.filter((s) => !keepByKey.has(stripTypeSuffix(s.nameAr)));
  if (toDeactivate.length > 0) {
    console.log(`\n🗑 Soft-deleting ${toDeactivate.length} schools not in the official list:`);
    for (const s of toDeactivate) {
      console.log(`  - ${s.governorateId} | ${s.nameAr} (${s.code})`);
      await db.school.update({ where: { id: s.id }, data: { isActive: false } });
    }
  } else {
    console.log("\n✓ All existing schools match the official list");
  }

  // Audit log
  await db.auditLog.create({
    data: {
      userId: null,
      action: "BULK",
      entity: "School",
      entityId: "sync-moe-2026-2027",
      newValue: JSON.stringify({
        note: "Synced schools with MoE 2026/2027 official list (99 schools across 25 governorates)",
        created, updated, deactivated: toDeactivate.length,
      }),
      summary: `Sync schools from MoE list: created ${created}, updated ${updated}, deactivated ${toDeactivate.length}`,
    },
  });

  // Final summary
  const summary = await db.school.groupBy({
    by: ["type"],
    where: { isActive: true, isArchived: false },
    _count: true,
  });
  console.log(`\n📊 Final active totals:`);
  for (const s of summary) console.log(`  ${s.type}: ${s._count}`);

  const arabicGovs = await db.school.findMany({
    where: { isActive: true, isArchived: false, type: "ARABIC" },
    select: { governorateId: true },
    distinct: ["governorateId"],
  });
  const govsWithArabic = await db.governorate.findMany({
    where: { id: { in: arabicGovs.map((s) => s.governorateId) } },
    select: { nameAr: true },
    orderBy: { sortOrder: "asc" },
  });
  console.log(`\n🏛 Governorates with Arabic schools (${govsWithArabic.length}):`);
  for (const g of govsWithArabic) console.log(`  - ${g.nameAr}`);

  console.log(`\n✅ Done. Created ${created}, updated ${updated}, deactivated ${toDeactivate.length}.`);
}

main()
  .catch((e) => { console.error(e); process.exit(1); })
  .finally(async () => { await db.$disconnect(); });