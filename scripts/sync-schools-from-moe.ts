// ─── Type extraction ────────────────────────────────────────────────
// "- عربى" → "ARABIC", "- لغات" → "LANGUAGES", otherwise LANGUAGES
// Match pattern: optional whitespace, dash, optional whitespace, type word,
// optional trailing whitespace at end of string.
function extractType(nameAr: string): "ARABIC" | "LANGUAGES" {
  // Arabic suffix: "- عربي" / "- عربى" / " -عربي " etc.
  if (/\s*-\s*عرب[ىي]\s*$/.test(nameAr)) return "ARABIC";
  return "LANGUAGES";
}

// Strip the trailing "- عربى" / "- لغات" suffix from the display name
function stripTypeSuffix(nameAr: string): string {
  return nameAr
    .replace(/\s*-\s*عرب[ىي]\s*$/, "")
    .replace(/\s*-\s*لغات\s*$/, "")
    .trim();
}

